package bean;

import java.sql.Date;

public class EventoBean {
	private String palinsesto;
	private String evento;
	private Date data;
	private String squadraCasa;
	private String squadraTrasferta;
	private String quota1;
	private String quotaX;
	private String quota2;
	
	
	public String getPalinsesto() {
		return palinsesto;
	}
	public void setPalinsesto(String palinsesto) {
		this.palinsesto = palinsesto;
	}
	public String getEvento() {
		return evento;
	}
	public void setEvento(String evento) {
		this.evento = evento;
	}
	public Date getData() {
		return data;
	}
	public void setData(Date data) {
		this.data = data;
	}
	public String getSquadraCasa() {
		return squadraCasa;
	}
	public void setSquadraCasa(String squadraCasa) {
		this.squadraCasa = squadraCasa;
	}
	public String getSquadraTrasferta() {
		return squadraTrasferta;
	}
	public void setSquadraTrasferta(String squadraTrasferta) {
		this.squadraTrasferta = squadraTrasferta;
	}
	public String getQuota1() {
		return quota1;
	}
	public void setQuota1(String quota1) {
		this.quota1 = quota1;
	}
	public String getQuotaX() {
		return quotaX;
	}
	public void setQuotaX(String quotaX) {
		this.quotaX = quotaX;
	}
	public String getQuota2() {
		return quota2;
	}
	public void setQuota2(String quota2) {
		this.quota2 = quota2;
	}
	
	
}
